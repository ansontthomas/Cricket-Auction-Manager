class Player {
    constructor(id, name, age, role, basePrice, soldPrice, isSold) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.role = role;
        this.basePrice = basePrice;
        this.soldPrice = soldPrice;
        this.isSold = isSold;
    }
}
export default Player;
